using Mamey;
using Microsoft.Extensions.Logging;
using Pupitre.Ministries.Domain.Entities;
using Pupitre.Ministries.Domain.Repositories;
using Pupitre.Ministries.Infrastructure.EF.Repositories;
using Pupitre.Ministries.Infrastructure.Mongo.Repositories;
using Pupitre.Ministries.Infrastructure.Redis.Repositories;

namespace Pupitre.Ministries.Infrastructure.Composite;

internal class CompositeMinistryDataRepository : IMinistryDataRepository
{
    private readonly MinistryDataPostgresRepository _postgresRepo;
    private readonly MinistryDataMongoRepository _mongoRepo;
    private readonly MinistryDataRedisRepository _redisRepo;
    private readonly ILogger<CompositeMinistryDataRepository> _logger;

    public CompositeMinistryDataRepository(
        MinistryDataPostgresRepository postgresRepo,
        MinistryDataMongoRepository mongoRepo,
        MinistryDataRedisRepository redisRepo,
        ILogger<CompositeMinistryDataRepository> logger)
    {
        _postgresRepo = postgresRepo;
        _mongoRepo = mongoRepo;
        _redisRepo = redisRepo;
        _logger = logger;
    }

    public async Task AddAsync(MinistryData entity, CancellationToken cancellationToken = default)
    {
        await _postgresRepo.AddAsync(entity, cancellationToken);
        await TryAsync(() => _redisRepo.AddAsync(entity, cancellationToken), "Redis");
        await TryAsync(() => _mongoRepo.AddAsync(entity, cancellationToken), "MongoDB");
    }

    public async Task<MinistryData> GetAsync(MinistryDataId id, CancellationToken cancellationToken = default)
    {
        try { var r = await _redisRepo.GetAsync(id, cancellationToken); if (r != null) return r; }
        catch { _logger.LogWarning("Redis read failed, trying MongoDB"); }
        try { var m = await _mongoRepo.GetAsync(id, cancellationToken); if (m != null) return m; }
        catch { _logger.LogWarning("MongoDB read failed, trying PostgreSQL"); }
        return await _postgresRepo.GetAsync(id, cancellationToken);
    }

    public async Task UpdateAsync(MinistryData entity, CancellationToken cancellationToken = default)
    {
        await _postgresRepo.UpdateAsync(entity, cancellationToken);
        await TryAsync(() => _redisRepo.UpdateAsync(entity, cancellationToken), "Redis");
        await TryAsync(() => _mongoRepo.UpdateAsync(entity, cancellationToken), "MongoDB");
    }

    public async Task DeleteAsync(MinistryDataId id, CancellationToken cancellationToken = default)
    {
        await _postgresRepo.DeleteAsync(id, cancellationToken);
        await TryAsync(() => _redisRepo.DeleteAsync(id, cancellationToken), "Redis");
        await TryAsync(() => _mongoRepo.DeleteAsync(id, cancellationToken), "MongoDB");
    }

    public async Task<bool> ExistsAsync(MinistryDataId id, CancellationToken cancellationToken = default)
    {
        try { if (await _redisRepo.ExistsAsync(id, cancellationToken)) return true; } catch { }
        try { if (await _mongoRepo.ExistsAsync(id, cancellationToken)) return true; } catch { }
        return await _postgresRepo.ExistsAsync(id, cancellationToken);
    }

    public Task<IReadOnlyList<MinistryData>> BrowseAsync(CancellationToken cancellationToken = default)
        => _postgresRepo.BrowseAsync(cancellationToken);

    private async Task TryAsync(Func<Task> action, string store)
    {
        try { await action(); }
        catch (Exception ex) { _logger.LogWarning(ex, "Propagation to {Store} failed", store); }
    }
}
